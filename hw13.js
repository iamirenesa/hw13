// Реалізувати рекурсивну функцію, яка зводить число в ступінь:
// Число, яке треба піднести до ступеню, передається як перший аргумент у функції;
// Ступінь передається як другий аргумент у функцію pow (num, degree).

function pow(a, b){
    if(b === 1){
        return a;
    } else {
        return a * pow(a, b - 1);
    }
}
